<p class="profile-text">   
    &ensp; &ensp; Kai was born in Suncheon, South Jeolla, he is the younger brother of three children, with two older sisters. 
    He began to train in ballet while in third grade after he watched the The Nutcracker, however started dancing when he was 
    eight years old in the fourth grade of elementary school, and originally did jazz dance. He stated that his parents initially 
    wanted him to learn Taekwondo and the piano.
     <br><br>
    &ensp; &ensp; Kai became the first public member of the EXO men's group launched by SM Entertainment on December 23, 2011. 
    He made his official debut as a member of EXO group on April 8, 2012. In December 2019, he participated in Gucci's "The Performers" 
    project, shooting a film about his growth story. The film mentioned that his deceased father was the most important benefactor 
    of his encounter with dance, which was of great significance to his life. After participating in the SM Youth Best Contest with
     the encouragement of his father, he won and signed with the company in 2007 at thirteen years old. He began training in 
     hip hop after joining SM Entertainment. Kai graduated from School of Performing Arts Seoul in 2012.
    <br><br>
    &ensp; &ensp; On August 7, 2019, Kai was confirmed to be a member of SuperM, a "K-pop supergroup" created by SM Entertainment in 
    collaboration with Capitol Records. The group's promotions are scheduled to begin in October and are aimed at the American market. 
    Kai has been selected as the first Gucci Eyewear Men Global Ambassador in Korea. SuperM debuted with the group’s self-titled 
    debut EP on October 4, 2019.
