<p class="profile-text">   
    J-Hope released his first solo mixtape, Hope World, worldwide on March 1, 2018. The album was met with a positive reception. 
    His debut at number 63 (and subsequent peak at number 38) made him the highest-charting solo Korean artist on the Billboard 200 
    at the time of the mixtape's release. On September 27, 2019, he released his song ”Chicken Noodle Soup” featuring Becky G. On 
    October 12, 2019, the solo single debuted at No. 81 on US Billboard Hot 100, which made him the first member of BTS with a hot 
    100 hit under his own name. 
    <br><br>
    &ensp; &ensp; J-Hope was born as Jung Ho-seok (Korean: 정호석) on February 18, 1994, in Gwangju, South Korea, where he lived with 
    his parents and older sister. Before debuting with BTS, he was part of the underground dance team Neuron and took dance classes at Gwangju 
    Music Academy for six years, from 4th grade to his 1st year in high school, when he signed his contract with Big Hit Entertainment. 
    J-Hope had been relatively well known for his skills in dance prior to his debut; he won various local prizes for dance, including 
    placing first in a national dance competition in 2008. His skills in dance eventually led him to gain interest in singing, helping 
    him to audition as an idol trainee. As a trainee, J-Hope was featured as a rapper in Jo Kwon's song "Animal", which was released in 2012.
     <br><br>
    &ensp; &ensp; J-Hope has been described as having an upbeat and energetic tone to his music and performances. His mixtape, Hope World, 
    was described as having a fun nature and variety of musical genres, including synth-pop, trap, house, alternative hip hop, funk-soul, 
    and retro elements. Jeff Benjamin of Fuse wrote that the atmospheric style of "Blue Side", Hope World's outro track, "leaves 
    the listener curious for what's coming next from J-Hope." The lyrical elements of the mixtape, notably the lead song "Daydream", 
    was praised by Billboard magazine for its discussion of the difficulties an idol faces in their career, various literary references, 
    and fun presentation of the serious subject matter.

    J-Hope cites the adventurous nature of Jules Verne's Twenty Thousand Leagues Under the Sea and the works of Kyle, Aminé, 
    and Joey Badass as influencers on his style and work on Hope World. The idea of peace has also provided a basis for much of his lyrics, 
    stating that "it'd be fantastic to become a part of someone's personal peace through my music" in an interview with Time magazine. 
    The idea of "representing the modern generation" has also influenced his work on BTS' music. There was also a reference to Douglas 
    Adams' science fiction series The Hitchhiker's Guide to the Galaxy.
    
    He released his first mixtape, Hope World, on March 1, a mixtape that "showcases exceptional creativity, genuine personality, and 
    a cohesive sense of direction."
    
    In January 2020, J-Hope was promoted to a full member of the Korea Music Copyright Association.
