      <p class="bio-text">
        &ensp; &ensp; Having sold over 20 million albums on the Gaon Music Chart, BTS is the best-selling artist in South Korean history 
        and holds the best-selling album in South Korea with Map of the Soul: 7. BTS was ranked at number 45 on Billboard's Top Touring 
        Artists of the 2010s, the only non-English speaking act on the list. During their Love Yourself World Tour, BTS became the first 
        Asian and first non-English speaking act to headline and sell out Wembley Stadium and broke the record for the single highest-grossing 
        engagement in Rose Bowl Stadium history. 
        <br><br>
        &ensp; &ensp;
        Featured on Time's international cover as "Next Generation Leaders", BTS has also appeared 
        in the magazine's lists of the 25 most influential people on the internet (2017–2019) and the 100 most influential people in the 
        world (2019). Forbes Korea named BTS the most influential celebrities of Korea in 2018 and 2020. As of 2019, BTS is purportedly 
        worth more than $4.65 billion to South Korea's economy each year, or 0.3 percent of the country's GDP. They attract one in every 13 
        foreign tourists that visited South Korea and were cited as one of the key acts in boosting global music sales to $19 billion in 2018.  
        <br><br>
        &ensp; &ensp; 
        On August 31, BTS made their MTV Video Music Awards (VMAs) debut with the first live performance of "Dynamite" 
        and won four awards: Best Group, Best Choreography, Best Pop Video, and Best K-pop (the last three for their music video for 
        "On"). They performed "Dynamite" and older tracks such as "Idol", "Home","Mikrokosmos",and "Black Swan" on 
        The Tonight Show for their week-long residency during the week of September 28. On October 14, they performed the single at the 
        2020 Billboard Music Awards and won the Top Social Artist award, becoming their fourth consecutive year of winning the award. 
      </p>

