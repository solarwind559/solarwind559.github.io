  <h1>EXO</h1>

  <p class="bio-text">  &ensp; &ensp; EXO (Korean: 엑소) is a South Korean-Chinese boy band based in Seoul, 
    consisting of nine members: Xiumin, Suho, Lay, Baekhyun, Chen, Chanyeol, D.O., Kai and Sehun. 
    The band was formed by SM Entertainment in 2011 and debuted in 2012. Their music — released and performed in Korean, Mandarin, 
    and Japanese — primarily incorporates pop, hip-hop, and R&B, including elements of electronic dance music genres such as house 
    and trap. The band ranked as one of the top five most influential celebrities on the Forbes Korea Power Celebrity list from 
    2014 to 2018 and have been labeled "Kings of K-pop" and the "Biggest boyband in the world" by various media outlets. 
    <br>
  </p>

