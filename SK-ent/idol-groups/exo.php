  <p class="bio-text">
    &ensp; &ensp; Exo have won numerous awards throughout their career, including five consecutive Album of the Year 
    awards at the Mnet Asian Music Awards and two consecutive Artist of the Year awards at the Melon Music Awards, 
    and have performed over 100 concerts across four headlining tours and multiple joint tours. Outside of music, 
    the band members have endorsement deals with brands such as Nature Republic and Samsung and participate in philanthropic 
    efforts such as Smile For U, an ongoing project by SM Entertainment and UNICEF that began in 2015.
    <br><br>
    &ensp; &ensp;
    Exo have been praised for their synchronized choreography and elaborate stage designs. The band was choreographed early 
    in their career by American hip hop choreographers Tony Testa and Nicholas Bass for songs like "Wolf" and "Growl", 
    respectively. More recently, Exo collaborated with choreographer Kim Tae Woo. Dazed admired the band's 
    "seamless...knife-point choreography", saying it has put them "at the forefront of pop domination".
    <br><br>
    &ensp; &ensp; 
    Exo performed at the closing ceremony of the 2018 Winter Olympics in Pyeongchang on February 25, serving as representatives 
    of K-pop along with CL. Prior to the performance, Baekhyun sang the national anthem at the opening ceremony of the 
    International Olympic Committee general assembly on February 5, and Exo performed at the official D-100 Concert in late 2017, 
    which marked 100 days until the start of the Winter Olympics. The band's performance drew widespread global media 
    attention and praise. In April 2018, the Korean Mint Corporation released official 
    commemorative medals for Exo, honoring the group for their contribution to the global spread of Korean culture. 
    The nine medals, each commemorating one of the group's members, were unveiled at a ceremony in Seoul.
    <br><br>
    &ensp; &ensp;
    In February 2020, SM Entertainment revealed that Exo members would prioritize solo and unit activities for 2020, due to 
     from the group by several members while serving their mandatory military enlistment. As of December 2020, Exo has five 
     active members: Xiumin, Baekhyun, Chanyeol, Kai, and Sehun.
  </p>

